from flask import Blueprint, request, jsonify, render_template, send_file
from io import BytesIO
from reportlab.lib.pagesizes import letter
from reportlab.pdfgen import canvas

from .auth import generate_token, auth_required
from .validation import validate_payload
from .ml_service import predict
from .explain_service import generate_explanation
from .recommend_service import generate_recommendations
from .llm_service import chat_with_assistant, extract_medical_values

bp = Blueprint("main", __name__)

# ---------------- Home & UI ----------------

@bp.route("/")
def home():
    return "Backend Running Successfully!"

@bp.route("/chat")
def chat():
    return render_template("chat.html")

# ---------------- Auth ----------------

@bp.route("/api/login", methods=["POST"])
def login():
    data = request.get_json()
    email = data.get("email", "")
    if not email:
        return jsonify({"error": "Email required"}), 400
    return jsonify({"token": generate_token(email)})

# ---------------- LLM Chat ----------------

@bp.route("/api/llm", methods=["POST"])
def api_llm():
    data = request.get_json() or {}
    user_msg = data.get("message", "")
    history = data.get("history", [])
    answers = data.get("answers", {}) or {}

    reply = chat_with_assistant(history, user_msg, answers)

    extracted = extract_medical_values(user_msg)

    updated_answers = answers.copy()
    updated_answers.update(extracted)

    return jsonify({
        "reply": reply,
        "extracted": extracted,
        "answers": updated_answers
    })

# ---------------- Prediction API ----------------

@bp.route("/api/predict", methods=["POST"])
@auth_required
def api_predict():
    data = request.get_json() or {}

    try:
        features, warnings = validate_payload(data)
    except ValueError as e:
        return jsonify({"error": str(e)}), 400

    result = predict(features)
    explanation = generate_explanation(features, result["probability"])
    recs = generate_recommendations(features, result["probability"])

    return jsonify({
        "prediction": result["prediction"],
        "probability": result["probability"],
        "explanation": explanation,
        "warnings": warnings,
        "recommendations": recs
    })

# ---------------- PDF Report API ----------------

@bp.route("/api/report_pdf", methods=["POST"])
@auth_required
def api_report_pdf():
    data = request.get_json() or {}

    answers = data.get("answers", {}) or {}
    result = data.get("result", {}) or {}
    explanation = data.get("explanation", {}) or {}
    recommendations = data.get("recommendations", []) or []
    warnings = data.get("warnings", []) or []
    history = data.get("history", []) or []

    buffer = BytesIO()
    c = canvas.Canvas(buffer, pagesize=letter)
    width, height = letter

    y = height - 50
    c.setFont("Helvetica-Bold", 16)
    c.drawString(50, y, "Diabetes Risk Assessment Report")
    y -= 30

    c.setFont("Helvetica", 10)
    c.drawString(50, y, "This report is generated by an AI assistant and is not a medical diagnosis.")
    y -= 20

    # Answers
    c.setFont("Helvetica-Bold", 12)
    c.drawString(50, y, "Input Values")
    y -= 18
    c.setFont("Helvetica", 10)
    for k, v in answers.items():
        c.drawString(60, y, f"{k}: {v}")
        y -= 14
        if y < 80:
            c.showPage()
            y = height - 50

    # Result
    c.setFont("Helvetica-Bold", 12)
    c.drawString(50, y, "Model Result")
    y -= 18
    c.setFont("Helvetica", 10)
    if "probability" in result:
        prob_percent = float(result["probability"]) * 100
        c.drawString(60, y, f"Estimated Risk Probability: {prob_percent:.1f}%")
        y -= 14
    if "prediction" in result:
        label = "High Risk" if int(result["prediction"]) == 1 else "Lower Risk"
        c.drawString(60, y, f"Risk Category: {label}")
        y -= 14

    # Explanation
    c.setFont("Helvetica-Bold", 12)
    c.drawString(50, y, "Explanation")
    y -= 18
    c.setFont("Helvetica", 10)
    summary = explanation.get("summary", "N/A")
    c.drawString(60, y, f"{summary}")
    y -= 14
    for r in explanation.get("reasons", []):
        c.drawString(70, y, f"- {r}")
        y -= 14
        if y < 80:
            c.showPage()
            y = height - 50

    # Recommendations
    c.setFont("Helvetica-Bold", 12)
    c.drawString(50, y, "General Suggestions")
    y -= 18
    c.setFont("Helvetica", 10)
    for rec in recommendations:
        c.drawString(60, y, f"- {rec}")
        y -= 14
        if y < 80:
            c.showPage()
            y = height - 50

    # Warnings
    if warnings:
        c.setFont("Helvetica-Bold", 12)
        c.drawString(50, y, "Value Warnings")
        y -= 18
        c.setFont("Helvetica", 10)
        for w in warnings:
            c.drawString(60, y, f"- {w}")
            y -= 14
            if y < 80:
                c.showPage()
                y = height - 50

    # Conversation history
    if history:
        c.showPage()
        y = height - 50
        c.setFont("Helvetica-Bold", 12)
        c.drawString(50, y, "Conversation Summary")
        y -= 20
        c.setFont("Helvetica", 9)
        for msg in history:
            role = msg.get("role", "user")
            prefix = "User: " if role == "user" else "Assistant: "
            line = prefix + msg.get("content", "")
            c.drawString(50, y, line[:100])
            y -= 12
            if y < 80:
                c.showPage()
                y = height - 50

    c.showPage()
    c.save()
    buffer.seek(0)

    return send_file(
        buffer,
        as_attachment=True,
        download_name="diabetes_risk_report.pdf",
        mimetype="application/pdf",
    )
